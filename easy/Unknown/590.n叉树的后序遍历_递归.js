/**
 * // Definition for a Node.
 * function Node(val,children) {
 *    this.val = val;
 *    this.children = children;
 * };
 */

/**
 * @param {Node} root
 * @return {number[]}
 */
var postorder = function (root) {
    if (!root) return [];
    let res = [];
    for (let i = 0; i < root.children.length; i++) {
        res = res.concat(postorder(root.children[i]));
    }
    return res.concat(root.val);
};
