/**
 * Definition for a binary tree node.
 * class TreeNode {
 *     val: number
 *     left: TreeNode | null
 *     right: TreeNode | null
 *     constructor(val?: number, left?: TreeNode | null, right?: TreeNode | null) {
 *         this.val = (val===undefined ? 0 : val)
 *         this.left = (left===undefined ? null : left)
 *         this.right = (right===undefined ? null : right)
 *     }
 * }
 */

function inorderTraversal(root: TreeNode | null): number[] {
    const res: number[] = [];
    let cur = root;
    let pre: TreeNode;
    while (cur !== null) {
        if (!cur.left) {
            res.push(cur.val);
            cur = cur.right;
        } else {
            pre = cur.left;
            while (pre.right) {
                pre = pre.right;
            }
            pre.right = cur;
            let temp = cur;
            cur = cur.left;
            temp.left = null;
        }
    }
    return res;
};
